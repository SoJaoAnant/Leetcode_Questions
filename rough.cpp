#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 15;
    int b = 7;
    int c;

    c = a & b;
    cout << c << endl;

    c = a | b;
    cout << c << endl;

    c = ~a;
    cout << c << endl;

    c = a << 5;
    cout << c << endl;

    c = a >> 1;
    cout << c << endl;

    return 0;
}